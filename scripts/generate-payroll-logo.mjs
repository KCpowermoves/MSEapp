#!/usr/bin/env node
// Resize public/logo.png to a PDF-embedding-friendly 256x256 PNG
// and emit it as a base64 constant in lib/payroll-logo.ts so the
// PDF generator can embed the real MSE logo without any runtime
// filesystem dependency on Vercel.
//
// Run after the logo asset changes:
//   node scripts/generate-payroll-logo.mjs

import sharp from "sharp";
import { writeFileSync } from "node:fs";
import path from "node:path";

const SRC = path.resolve("public/logo.png");
const OUT = path.resolve("lib/payroll-logo.ts");

// Output JPEG (not PNG) with a solid white background. PDFKit's
// standalone PNG decoder has been flaky on Vercel for RGBA inputs;
// flattening to a white-background JPEG sidesteps the issue entirely
// and shrinks the asset by ~3x at visually-identical quality.
const buf = await sharp(SRC)
  .resize(256, 256, { fit: "inside" })
  .flatten({ background: { r: 255, g: 255, b: 255 } })
  .jpeg({ quality: 92, chromaSubsampling: "4:4:4" })
  .toBuffer();

const base64 = buf.toString("base64");
const mimeType = "image/jpeg";

const content = `// AUTO-GENERATED by scripts/generate-payroll-logo.mjs
// 256x256 white-background JPEG variant of public/logo.png, embedded
// as base64 so the payroll PDF generator can render the brand logo
// without depending on the deployment's filesystem layout.
//
// JPEG over PNG: PDFKit standalone is unreliable with RGBA PNGs on
// Vercel's serverless runtime. Flattening to a white-bg JPEG sidesteps
// the issue and shrinks the asset by ~3x at visually identical quality.
//
// To regenerate after a logo change: \`node scripts/generate-payroll-logo.mjs\`.

/* eslint-disable */
export const PAYROLL_LOGO_MIME = "${mimeType}";
export const PAYROLL_LOGO_BASE64 =
  "${base64}";

export function getPayrollLogoBuffer(): Buffer {
  return Buffer.from(PAYROLL_LOGO_BASE64, "base64");
}
`;

writeFileSync(OUT, content);
console.log(
  `wrote ${OUT} — ${buf.length} bytes (${base64.length} base64 chars)`
);
