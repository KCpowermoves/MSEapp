#!/usr/bin/env node
// Resize public/logo.png to a PDF-embedding-friendly 256x256 PNG
// and emit it as a base64 constant in lib/payroll-logo.ts so the
// PDF generator can embed the real MSE logo without any runtime
// filesystem dependency on Vercel.
//
// Run after the logo asset changes:
//   node scripts/generate-payroll-logo.mjs

import sharp from "sharp";
import { writeFileSync } from "node:fs";
import path from "node:path";

const SRC = path.resolve("public/logo.png");
const OUT = path.resolve("lib/payroll-logo.ts");

const buf = await sharp(SRC)
  .resize(256, 256, { fit: "inside" })
  .png({ compressionLevel: 9 })
  .toBuffer();

const base64 = buf.toString("base64");

const content = `// AUTO-GENERATED by scripts/generate-payroll-logo.mjs
// 256x256 PDF-optimized variant of public/logo.png, embedded as
// base64 so the payroll PDF generator can render the brand logo
// without depending on the deployment's filesystem layout.
// To regenerate after a logo change: \`node scripts/generate-payroll-logo.mjs\`.

/* eslint-disable */
export const PAYROLL_LOGO_BASE64 =
  "${base64}";

export function getPayrollLogoBuffer(): Buffer {
  return Buffer.from(PAYROLL_LOGO_BASE64, "base64");
}
`;

writeFileSync(OUT, content);
console.log(
  `wrote ${OUT} — ${buf.length} bytes (${base64.length} base64 chars)`
);
